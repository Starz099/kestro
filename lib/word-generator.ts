export const generateWordSequence = (
  count: number,
  wordBank: readonly string[] = WORD_BANK,
): string[] => {
  if (count <= 0 || wordBank.length === 0) {
    return [];
  }

  const words = new Array<string>(count);
  for (let index = 0; index < count; index += 1) {
    const randomIndex = Math.floor(Math.random() * wordBank.length);
    words[index] = wordBank[randomIndex];
  }

  return words;
};
